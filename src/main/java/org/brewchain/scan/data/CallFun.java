package org.brewchain.scan.data;

/**
 * 调用方法实体
 * @author KaminanGTO
 *
 */
public class CallFun {

	/**
	 * 方法hex
	 */
	private String funHex;
	
	/**
	 * 参数hex
	 */
	private String paramsHex;

	public String getFunHex() {
		return funHex;
	}

	public void setFunHex(String funHex) {
		this.funHex = funHex;
	}

	public String getParamsHex() {
		return paramsHex;
	}

	public void setParamsHex(String paramsHex) {
		this.paramsHex = paramsHex;
	}
	
	
}
